public class HesapMakinasi {

    public float topla(float sayi1, float sayi2) {
        return sayi1 + sayi2;
    }

    public float cikar(float sayi1, float sayi2) {
        return sayi1 - sayi2;
    }

    public float carp(float sayi1, float sayi2) {
        return sayi1 * sayi2;
    }

    public float bol(float sayi1, float sayi2) {
        return sayi1 / sayi2;
    }

    public float kare(int sayi){
        return (float) Math.pow(sayi,2);
    }

    public float kareKok(int sayi){
        return (float) Math.sqrt(sayi);
    }

}
